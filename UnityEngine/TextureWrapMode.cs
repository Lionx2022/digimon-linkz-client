using System;

namespace UnityEngine
{
	/// <summary>
	///   <para>Wrap mode for textures.</para>
	/// </summary>
	public enum TextureWrapMode
	{
		/// <summary>
		///   <para>Tiles the texture, creating a repeating pattern.</para>
		/// </summary>
		Repeat,
		/// <summary>
		///   <para>Clamps the texture to the last pixel at the border.</para>
		/// </summary>
		Clamp
	}
}
