using System;

namespace UnityEngine.VR
{
	/// <summary>
	///   <para>Enumeration of nodes which can be updated by VR input.</para>
	/// </summary>
	public enum VRNode
	{
		/// <summary>
		///   <para>Left Eye node.</para>
		/// </summary>
		LeftEye,
		/// <summary>
		///   <para>Right Eye node.</para>
		/// </summary>
		RightEye,
		/// <summary>
		///   <para>Node between left and right eyes.</para>
		/// </summary>
		CenterEye,
		/// <summary>
		///   <para>Head node.</para>
		/// </summary>
		Head
	}
}
